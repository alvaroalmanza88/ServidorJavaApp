    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        new Thread(new Runnable(){
                public void run()
                {
                    try{
   			yo = new ServerSocket(5000);
                        jTextArea1.append("Socket escuchando en puerto 5000"+'\n');
                        cliente = yo.accept();
                        jTextArea1.append("Ya se conecto el cliente"+'\n');
                        entrada = new BufferedReader(new InputStreamReader(cliente.getInputStream()));
                        salida = new DataOutputStream(cliente.getOutputStream());
                        do{
                        informacion = entrada.readLine();
                        jTextArea1.append("Llego: " + informacion +'\n');
                        salida.writeInt(informacion.length());
                        String [] palabras = informacion.split(" ");
                        salida.writeInt(palabras.length);
                        }while(informacion.length()!=0);
   		} catch (IOException e){
   			
                        jTextArea1.append(e.getMessage()+'\n');
                }
                    
                }
            }).start();

    }//GEN-LAST:event_jButton1ActionPerformed

